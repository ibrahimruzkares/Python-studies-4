string = "Hello Python"
print(string[::])    #tüm elemanlarını yaz
print(string[0:12])  #tüm elemanlarını yaz
print(string[:7])    #0'dan başlayıp 7.elemana kadar
print(string[4:])    #4'üncü elemandan başlayıp sonuna kadar
print(string[3:8])   #verilen aralıklardaki elemanlar
print(string[2::])   #2'üncü elemandan başlayıp sonuna kadar
print(string[::3])   #0'ıncı elemandan başlayıp sonra gelen her 3. elemanı yaz
print(string[::-1])  #Tersinden yazma
print(string[3::-1]) #3'üncü elemana kadar olanları tersten yazma


